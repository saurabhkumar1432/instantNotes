package com.voicenotesai.presentation.theme

import androidx.compose.ui.unit.dp

/**
 * Modern spacing constants as specified in the UI consolidation modernization requirements.
 * 16dp screen padding, 12dp component gaps
 */
object ModernSpacing {
    /**
     * Screen-level padding (16dp)
     */
    val screenPadding = 16.dp
    
    /**
     * Section spacing between major UI sections (16dp)
     */
    val sectionSpacing = 16.dp
    
    /**
     * Gap between components (12dp)
     */
    val componentGap = 12.dp
    
    /**
     * Standard card padding (16dp)
     */
    val cardPadding = 16.dp
    
    /**
     * Small card padding (12dp)
     */
    val smallCardPadding = 12.dp
    
    /**
     * Additional spacing values for consistency
     */
    val extraSmall = 4.dp
    val small = 8.dp
    val medium = 16.dp
    val large = 24.dp
    val extraLarge = 32.dp
}